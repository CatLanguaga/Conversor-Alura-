package FuncionalidadMoneda;

public class ConversorFuncion {
    
    
    public static double funcion(double valor, double divisa){
        
        double res = valor * divisa;
        
        return res;
   
    }
    
    public static double funcion2(double valor, double divisa){
        
        double res = valor / divisa;
        
        return res;
   
    }
}
